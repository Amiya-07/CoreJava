package Day_4;

public class CycleSelling {

	public static void main(String[] args)
	{
		double cycle=1200;
		double repair=250;
		double coloring=350;
		double newAccessories= 500;
		double profit=1500;
		double totalprice = cycle+repair+coloring+newAccessories+profit;
		System.out.println("The selling price of cycle is "+totalprice);
	}

}
/*
old cycle for Rs. 1200
spend Rs. 250 on repairs
 Rs. 350 in coloring 
 new accessories worth Rs. 500
  profit of Rs. 1500
*/